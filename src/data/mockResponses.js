// Session-level rotation tracker: prevents repeating the last N responses per character
const recentlyUsed = new Map()
const AVOID_LAST = 15

export function getResponse(characterId) {
  const responses = pool[characterId]
  if (!responses || responses.length === 0) return '...'

  const recent = recentlyUsed.get(characterId) || []

  // Filter out recently used indices
  const available = responses
    .map((text, idx) => ({ text, idx }))
    .filter(({ idx }) => !recent.includes(idx))

  // If everything is exhausted, reset and use the full pool
  const pick = available.length > 0
    ? available[Math.floor(Math.random() * available.length)]
    : { text: responses[Math.floor(Math.random() * responses.length)], idx: 0 }

  // Update recent tracker
  const updated = [...recent, pick.idx].slice(-AVOID_LAST)
  recentlyUsed.set(characterId, updated)

  return pick.text
}
